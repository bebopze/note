-- 算术运算
a, b = 10, 20

-- 加法
c = a + b
print(a .. "+" .. b .. "=" .. c)

--减法
c = a - b
print(a .. "-" .. b .. "=" .. c)

-- 乘法
c = a * b
print(a .. "*" .. b .. "=" .. c)

-- 除法
c = a / b
print(a .. "/" .. b .. "=" .. c)

-- 求余
c = a % b
print(a .. "%" .. b .. "=" .. c)

-- 乘方
c = a ^ 2
print(a .. "^2" .. "=" .. c)